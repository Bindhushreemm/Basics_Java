
public class exp4 {
	public static void main(String[] args) {
		int x=0,y=0;
		System.out.println(x++);//0
		System.out.println(x);//1
		System.out.println(++y);//1
		System.out.println(y);//1
		x=0;
		y=0;
		System.out.println(x--);
		System.out.println(x);
		System.out.println(--y);
		System.out.println(y);
		
		//relational operators
		int log=110;
		System.out.println((log>90));
		System.out.println((log<90));
		System.out.println((log>=90));
		System.out.println((log<=90));
		
		System.out.println((log!=90));
		
		
		
	}

}
